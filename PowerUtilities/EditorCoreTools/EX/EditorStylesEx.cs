        public static string GetEditorStyleNames(string outputTxtPath=null)
        {
            var sb = new StringBuilder();
            foreach (GUIStyle s in GUI.skin)
            {
                //ShurikenEmitterTitle = nameof(ShurikenEmitterTitle),
                var varName = Regex.Replace(s.name, "[ ,.]", "_");
                sb.AppendFormat("{0} = \"{1}\",\n", varName, s.name);
            }
            var result = sb.ToString();
            if (!string.IsNullOrEmpty(outputTxtPath))
            {
                File.WriteAllText(outputTxtPath, result);
            }
            return result;
        }

